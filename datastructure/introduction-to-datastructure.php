	<p class=col-text>An overview of data organization and certain data structures will be covered along with a discussion of the different operations which are applied to these data structures.Last, we will introduce the notion of an algorithm and its complexity, and we will discuss the time space tradeoff that may occue in choosing a particular algorithm and datastructure for a given problem.</p>
	<h4 class=headcol>Definition of Datastructure :</h4>
	<p class=col-text>Data may be organized in many different ways; the logical or mathematical model of a particular organization of data is called data structure.</p>
	<p class=col-text>The choice of a particular data model depends on two consideration. First, it must be rich enough in structure to mirror the actual relationships of the data in the real world. On the other hand, the structure should be simple enough that one can effectively process the data when necessary. </p>
	<h4 class=headcol>Classification of Datastructures :</h4>
	<p class=col-text>Data structures are generally classified into primitive and non-primitive data structure.Basic data types such as integer,real,character and boolean are known as primitive data structure.These data types consist of characters that cannot be divided, and hence they are also called simple data types.</p>
	<p class=col-text><b>Primitive Data Structures</b>:-Basic data types such as integer,real, character and boolean are known as primitive data structures.</p>
	<p class=col-text><b>Non-Primitive Data Structures</b>:-Linked-lists, stacks, queues, trees and graphs are examples of non-primitive data structures.</p>
	<p class=col-text>The simplest example of non-primitive data structure is the processing of complex numbers.Very few computers are capable of doing arithmetic on complex numbers. Linked-lists,stacks, queues, trees and graphs are examples of non primitive data structures.</p>
	<h4 class=headcol>Static and Dynamic Datastructures :</h4>
	<p class=col-text><b>Datastructures also can be classified into two ways:</b></p>
	<p class=col-text><b>1. Static data structure</b>It is a type of data structure where the size is allocated at the compile time. Therefore, the maximum size is fixed.</p>
	<p class=col-text><b>2. Dynamic data structure</b>It is a type of data structure where the size is allocated at the run time. Therefore, the maximum size is flexible.</p>

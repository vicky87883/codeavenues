	<h5 class=headcol>Linear-Datastructure :</h5>
	<p class=col-text>A datastructure is said to be linear if its elements form a sequence or a linear list. In linear data structures, the data is arranged in a linear fashion although the way they are stored in memory need not be sequential. Array, linked lists, stacks and queues are example of linear data structures.</p>
	<h5 class=headcol>Non-linear Datastructures :</h5>
	<p class=col-text>A Data structure is said to be non-linear if the data is not arranged in sequence. The insertion and deletion of data is therefore not possible in a linear fashion. Trees and graphs are example of non-linear data structures.</p>
	<h4 class=headcol>Data Structure Operations :</h4>
	<p class=col-text>The data appearing in our data structures are processed by means of certain operations. In fact, the particular data structure that one chooses for a given situation depends largely on the frequency with which specific operations are performed.Let's discuss all the operations one by one.</p>
	<p class=col-text>The following four operations play a major role in this text:</p>
	<p class=col-text><b>1. Traversing :</b> Accessing each record exactly once so that certain items in the record may be processed.(This accessing and processing is sometimes called "visiting" the record</p>
	<p class=col-text ><b>2. Searching :</b> Finding the location of the record with a given key value, or finding the locations of all records which satisfy one or more conditions.</p>
	<p class=col-text><b>3. Inserting: </b>
		Additing a new record to the structure.
	</p>
	<p class=col-text><b>4. Deleting :</b>Removing a record from the structure.</p>
	<p class=col-text>Sometimes two or more of the operations may be used in a given situation;e.g., we may want to delete the record with a given key, which may mean we first need to search for the location of the record.</p>
	<p class=col-text><b>1. Sorting :</b>Arranging the records in some logical order(e.g., alphabetically according to some NAME key, or in numerical order according to some NUMBER key, such as social security number or account number)</p>
	<p class=col-text><b>2. Merging :</b>Combining the records in two different sorted files into a single sorted file.</p>
